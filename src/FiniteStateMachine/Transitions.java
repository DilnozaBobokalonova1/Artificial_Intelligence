package FiniteStateMachine;

public class Transitions {
	int numOfMissionaries;
	int numOfCannibals;
	int goalSide;
	
	public Transitions(int numOfMissionaries, int numOfCannibals, int goalSide) {
		this.numOfMissionaries = numOfMissionaries;
		this.numOfCannibals = numOfCannibals;
		this.goalSide = goalSide;
	}
	
	
}
